<h3>概要</h3>

<p>
  1812年に完成した《交響曲第7番》は、双生児とされる《交響曲第8番》（1812）と同時に非公開で演奏されたのち、1813年に公開で初演された。初演は大成功をおさめ、特に第2楽章は単体でアンコールとして演奏されている。ベートーヴェン自身も本作を気に入っており、ロンドンの興行師ザロモンに向けた手紙の中で、「私のもっともすぐれた作品の一つ」と評した。
</p>

<p>
  ベートーヴェンの交響曲は、《交響曲第1番》（1800）を除けば偶数番のものは軽快優美、奇数番のものは豪放雄大であると言われるが、《第7番》はまさに「奇数番」的な作品である。楽章構成や形式はハイドンやモーツァルトのものに倣う。自身が《第6番》（1808）までに達成した楽章統一の観点からは、いずれの楽章の主題も、冒頭の特徴的なリズムと同音反復、およびそれに続く順次進行の動き、という同一性を有する。さらに、本作ではリズムにまつわる様々な試みが見られ、リストは「リズムの神化」、ワーグナーは「舞踏の神化」とこの作品を讃えている。
</p>

<h3>楽曲の展開</h3>

<h4>第1楽章　Poco sostenuto – Vivace</h4>

<p>
  イ長調。ソナタ形式をとる。長い序奏では、低音の半音階進行など、重要な素材が次々に現れる。木管とヴァイオリンによるホ音の応酬から流れるように提示部に入ると、フルートとオーボエが付点8分音符に始まるリズム動機を提出し、第1主題（譜例1）が現れる。リズム動機は楽章全体を貫いて反復され、第1主題の最後に現れる同音価動機（枠線部）など様々な要素と織りあわされていく。長いコーダでは、リズム動機がいつの間にか姿を消し、中低弦が第1主題冒頭に着想を得た刺繍音的音型を執拗に反復する。やがてリズム動機の世界に回帰したところで曲はクライマックスを迎え、リズムの奔流に押し流されるように走り去る。
</p>

