<h4>第2楽章　Allegretto</h4>

<p>
  イ短調。三部形式と捉えられる。本楽章では、主題（譜例2）1小節目の“長+短+短”という動機が主役となる。低弦を中心に、ゆったりとしたテンポで始まった曲は、主題を執拗に反復しながら弦楽器と管楽器の音域をいっぱいに使って徐々に盛り上がる。やがて沈み込むように弱まっていくと、各楽器群が切れ切れに旋律を奏で、最後は消え入るように終わる。
</p>

