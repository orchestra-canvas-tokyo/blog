<h4>第3楽章　Presto</h4>

<p>
  ヘ長調のスケルツォ楽章である。底抜けに明るいスケルツォ部分と対照的なゆったりとしたトリオ部分を行き来し、最後は再びトリオへ回帰するかと見せかけてあっさりと終わる。
</p>

<h4>第4楽章　Allegro con brio</h4>

<p>
  イ長調のソナタ形式による狂喜乱舞の楽章である。冒頭でリズム動機が力強く2回打ち出され、律動的な第１主題がヴァイオリンに現れる。突然ピアノになり嬰へ短調に転じると、滑るような第2主題が現れ、また熱を取り戻したのち提示部を反復する。展開部は第1主題の音型を元とし、再現部を経て、雄大なコーダに入る。第1楽章コーダを彷彿とさせる低音の半音階的なオスティナートの上で、第1主題が再発展され、狂乱のクライマックスを迎えると、楽章冒頭の動機で全曲を締めくくる。
</p>

<h3>最後に</h3>

<p>
  ベートーヴェンがリズムの可能性を追求した本作では、特定のリズムが様々な要素に出会い、時には形式やデュナーミクの気まぐれな変化を誘い、変容していく様子が描かれている。このように柔軟な機動性や、刻々と移り変わる表情は、前の時代にはなかった要素としてベートーヴェンが音楽作品にもたらしたものの一つであった。以前の作曲家が、交わることのない対照的な要素を唐突に並べることを常としていたのに対し、ベートーヴェンは、ちょうどレンブラントが絵画の世界で試みたように、光と影が織りなす絶え間のない変化を描き出そうとしたのである。
</p>

