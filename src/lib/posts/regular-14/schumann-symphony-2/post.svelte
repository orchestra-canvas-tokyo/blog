<h3>最後に</h3>

<p>
  シューマンの抱いた苦悩は、現代において我々が直面するそれとなんら変わりはない。仕事を諦めざるを得ないこと、周囲の活躍に自分だけが追いつけないこと、家族や親友が苦しんでいる・亡くなったのを知ること、これらは現代でも容易に経験することである。そしてその経験に対する本質的な救いは存在せず、与えられる精神的な苦痛は計り知れない。そのような世界にあって我々がいかに日々を過ごしていくかに思いを馳せた時に、ふとシューマンの楽曲に滲む愛と苦悩に共鳴させられるのかもしれない。
</p>

