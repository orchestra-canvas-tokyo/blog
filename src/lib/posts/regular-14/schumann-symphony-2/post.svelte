<h4>第3楽章 Adagio espressivo</h4>

<p>
  ハ短調、幻想曲風の楽章だが、構成は「A-B-A-C-A-B-A」とロンド的である。楽章を通じて主要主題（譜例4）が変形しながら扱われる。旋律や内声のリズムは、シューマンを1853年に訪ねて以来家族ぐるみで交流のあったブラームスの楽曲を想起させるものであり、師匠の風格を感じさせる。
</p>

<Figure src={example4} caption="譜例4" maxHeightPx={90} />

<h4>第4楽章 Allegro molto vivace</h4>

<p>ハ長調、前半が節約されたソナタ形式、後半は前半と異なる主題を用いた自由な形式である。</p>
<p>
  序奏（譜例5）の後に行進曲風の第1主題（譜例6）が力強く現れ、展開される。第1主題と、その直後に奏でられる副主題部の3連音型は、シューベルトの大ハ長調交響曲を想起させ、シューマンのシューベルトへの尊敬を仄かに感じられる。副主題部の後には第3楽章の主題冒頭の変形が奏でられ、高潮して第1主題へと復帰していき、展開的再現部に入る。主題が再現されていく中で響くのは序奏（譜例5）と第1楽章冒頭のモットー（譜例1）であり、次第に収まって前半を終える。
</p>
<p>
  後半は全く異なる主題（譜例7）を用いて、前半部や第1楽章の材料を織り交ぜながら進んでいき、全曲の終結部である印象を覚える。最後は、ティンパニの連打と力強いハ長調の和音で締め括られる。
</p>

