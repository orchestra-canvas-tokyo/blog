<blockquote>
  この数日間、トランペットとティンパニの音が、頭のなかを鳴り続けています。ここから何が生まれるのか、私にはまだわかりません。
</blockquote>

<p>
  上記の文は本解説の執筆にあたって参照した複数の文献で取り上げられている。1845年9月にメンデルスゾーンに宛てて書かれた手紙の一節は、本作品の最初のインスピレーションを示すと同時に、シューマン自身の精神状態を端的に表している。
</p>
<p>
  本演奏会で扱うメンデルスゾーンとシューマンは同時代に活躍し、互いを認め合った存在である。そして、家族をこよなく愛し、その喪失によるショックが間接的に夭折を招いたといえる点でも似ているといえよう。メンデルスゾーンの場合は、姉ファニーが脳卒中で亡くなったことが健康状態悪化を招き、シューマンの場合は、幼少期に家族と離れたこと・喪ったことがその後の人生にも付き纏ったとされている。
</p>
<p>
  彼らの音楽は愛に溢れているが、ある種の不安も渦巻いている。しかしながら、その感情こそが彼らの音楽の魅力に寄与しているともいえる。
</p>

<h3>作曲までの歩み</h3>

<p>
  シューマンは1810年にドイツ、ライプツィヒ近郊の町、ツヴィッカウで末子として生まれた。出版社を興した勤勉な父に対して「心のこもった愛情あふれる教育を受けた」と自身でも回想している通り、暖かい両親と兄弟に囲まれていたことは確かである。その家庭にあってシューマンは手厚い初等教育を受け、音楽や詩に傾倒した。しかし、3-5歳の頃に代理母に預けられて家族との精神的距離が離れる経験をしたことや、16歳頃には姉の死（神経病による自殺と言われる）、次いで父の死を経験したことは、その後の人生に影を落としたといえよう。特に父はシューマンの音楽活動を最も理解していた家族であったため、その喪失感はとても大きかったであろう。また、1826年にウェーバー、1827年にベートーヴェン、1828年にシューベルトと、偉大な作曲家がこの世を去っている。特にシューベルトを崇拝と呼べるほど敬愛していたシューマンにとっては、彼の死も、大切な人を引き摺り込んでいく「死」への恐怖心を植え付けるに足るものであったと思われる。以降、彼は度々精神の不調をきたすことになる。
</p>
<p>
  その後シューマンは、1828年からフリードリヒ・ヴィークの元でピアノを学んだ。フリードリヒの娘で、後に妻となるクララと出会ったのもこの頃である。しかし、1832年に自作の装置を使った練習により指が不自由となってしまったため、ピアニストの道は絶たれてしまった。そこで作曲家に注力し、かたや1834年には『音楽新報』を創刊してロマン主義者を未来の担い手として賞賛する等、音楽評論家としても活躍した。
</p>
<p>
  著名なピアニストとして大成したクララとの結婚には、フリードリヒの反対もあり裁判にまで至った。結局、1840年に結婚許可判決を得る。幸せな結果だったが、苦悩も経験することになる。1842年にシューマンはクララの演奏旅行に帯同したが、その道中でクララのみが晩餐会に呼ばれるという事件があったのである。クララが「結婚以来最もおぞましい日」と述懐しているこの事件により、シューマンは、妻が自身よりも世間に求められていると感じ、精神を病んでしまった。これと同様の出来事は1844年にもあり、同様に精神を病んだシューマンは、ついに本解説冒頭で引用した旋律を幻聴として聴くことになる。
</p>

<h3>楽曲解説</h3>

<p>
  1846年11月にメンデルスゾーンの指揮により初演された本作品は、まさにシューマンの精神の浮き沈みが激しい時期に書かれた。シューマンは1844年に発症した精神疾患が回復せず、『音楽新報』の編集から退き、転居もして療養に努めた。その甲斐あって小康状態となった1845年12月に本作品のスケッチが作られた。しかし、その後も発作に阻まれ、完成には1846年10月までを要した。
</p>
<p>
  シューマンの楽曲はいずれも何かから受けた啓示に基づいて作曲されていると言われており、交響曲についても標題音楽である交響詩の形態に近いともいわれる。確かに本作品でも幻聴が「啓示」となっている。
</p>
<p>
  シューマンは脳内に執拗に鳴り響く音を五線譜に投影することで、なんとか苦しみから逃れようとしていたのかもしれない。
</p>

<h4>第1楽章 Sostenuto assai – Allegro ma non troppo ハ長調</h4>

<p>
  導入部では、ホルン、トランペット、アルト・トロンボーンが、全曲のモットーのように扱われる動機（譜例1）を提示する。これがシューマンの脳内に鳴り響いた旋律と推察される。その後、第1主題のリズムを暗示するように運動性を増すが、一旦冒頭の動機に戻って終わる。
</p>
<p>
  主部は第2主題が不明瞭なソナタ形式である。提示部では第1主題（譜例2）がリズムを発展させながら奏でられ、それをさまざまな動機が彩る。途中でト長調に変化した箇所からが第2主題とされるが、リズムは第1主題と同様である。
</p>
<p>展開部では第1主題はなりをひそめ、主部で主題を彩った様々な動機が活躍する。</p>
<p>
  再現部が提示部と同様に経過し、終結部では導入部の金管のモットーが現れつつ、第1主題のリズムが華々しく楽章を締め括る。
</p>

