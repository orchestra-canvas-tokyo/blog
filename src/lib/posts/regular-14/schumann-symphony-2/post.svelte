<h4>第2楽章 Scherzo Allegro vivace</h4>

<p>
  ハ長調、トリオを2つ持つスケルツォであり、3回のスケルツォの合間にそれぞれトリオが奏でられる。スケルツォ部分ではいずれも弦が主体となり、16分音符のパッセージ（譜例3）を奏でており、その響きはメンデルスゾーンを想起させ、彼らの親友関係を認識させてくれる。
</p>
<p>
  第1トリオでは管楽器の3連音楽句が、第2トリオでは4分音符を主体とした牧歌的で落ち着いた旋律が奏でられる。最後のコーダも16分音符で締められるが、第1楽章冒頭のモットー（譜例1）が再度現れる点にもご注目いただきたい。
</p>

