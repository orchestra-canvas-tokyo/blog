<p>
  近代社会の到来と共に芸術のあり方が四散していった20世紀初頭、作曲家として円熟期を迎えていたセルゲイ・ラフマニノフは、交響詩《死の島》（1909）を発表した。ラフマニノフは当時絶大な人気を博していた同名の絵画に薫染され、その死生観の彼方に本作品を創造した。一枚の静止画から管弦楽作品へ、静と動、光と音を紡いだ標題を紐解いていく。
</p>

<h3>絵画『死の島』の主題</h3>

<p>
  本作品の題材となったのは、スイス人画家アルノルト・ベックリン（1827–1901）による油彩画『死の島』である。ベックリンは象徴主義の画家であり、神話や聖書、文学などに宿る形而上の観念を、写実的だが神秘的な描写によって巧みに顕した。ベックリンは8人もの子供を病気で亡くしており、『死の島』からは彼の死生観が鮮明に読み取れる。静寂の海に浮かぶ島影には、死を象徴する糸杉が茂る。即ちこの孤島は死者の魂が眠る場所であり、海はこの世と冥界との狭間である。あらがえぬ死を迎えた者が棺桶に入り、「死の島」へと小舟で運ばれてゆく。不可逆な死への航路を、緻密な画法が愁然と映し出す。
</p>

