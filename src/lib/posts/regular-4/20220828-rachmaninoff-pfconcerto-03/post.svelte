<p>
  《ピアノ協奏曲第3番》は1909年夏、ラフマニノフが避暑地・作曲地として愛した、モスクワ近郊のイワノフカの別荘で作曲された。当代最高のピアニストでもあったラフマニノフが、初の訪米演奏会に向けてそのヴィルトゥオーゾの粋を集めて作曲した本作は、世界で最も難しいピアノ協奏曲の一つとして名高い。本作の献呈を受けたヨゼフ・ホフマンが演奏できなかったことや、ウラディーミル・ホロヴィッツやヴァン・クライバーンといった不世出のピアニストたちを発見したことが、本作の難易度の高さの証左である。
</p>

<p>
  初演は1909年11月28日に本人演奏で行われた。2回目の演奏は、初演を聴いて本作に惚れ込んだグスタフ・マーラーの指揮の下、翌年1月16日に実現した。
</p>

<h3>ラフマニノフとアメリカ</h3>

<p>
  当時の米国では、急速な工業化により全世界のピアノの半分以上が作成されており、各家庭へのピアノの普及率も高く、更に演奏家への支援も手厚かった。ピアノメーカーとして名高いスタインウェイ&サンズ社が、アントン・ルビンシテインやイグナツィ・ヤン・パデレフスキといった著名なピアノ演奏家の米国公演を斡旋していた。
</p>

<p>
  優れたピアノが生産され、演奏家にも手厚い米国が、作曲家兼ピアニストとしてのラフマニノフの後の亡命先として最適だったのであろう。そして、ラフマニノフと米国を繋いだ最初の架け橋こそ、紛れもない本作なのである。
</p>

<h3>楽曲の展開</h3>

<p>
  ラプソディとシンフォニーを組み合わせたような曲想である本作は、一般的な協奏曲同様、「急・緩・急」の3楽章形式である。
</p>

<h4>第1楽章 - Allegro ma non tanto</h4>

<p>随所に散りばめられたカデンツァが魅力的な、再現部を短縮したソナタ形式の楽章である。</p>

<p>
  波を想起させる音型に乗ってピアノが奏で始める冒頭の第1主題は、簡明で語りかけるような美しい旋律であり、全曲を貫く共通主題となる。本主題は、典礼音楽《Thy
  tomb, O Saviour, soldiers
  guarding.》や、ラフマニノフが古聖歌旋律を基に作り上げた《徹夜祷》の第10番（1915）〈ハリストスの復活を見て〉に酷似している。ただし、ラフマニノフは音楽学者ヨゼフ・ヤッセルに宛てた手紙で以下のように記している。
</p>

<blockquote>
  私はこのテーマに民謡、又は、典礼的な性質を当てはめたくなかったのです。もしそうしていたら、私はきっと、「意識的」にド#を許さずに、全体をド♮で維持する音階を守ったことでしょう。と同時に、このテーマが心ならずも、民謡か、あるいは典礼的な性質を帯びてしまっていることは自覚しています。
</blockquote>

<p>
  このように本主題は、自然短音階ではなく導音を用いて旋法的性質からの回避を試みている。しかし、狭音域での平坦さ故か、その試みに失敗していることをラフマニノフ自身も自覚している。しかも、ラフマニノフ自身の第1主題のピアノ演奏が、あっさりと流れるような演奏である点も、聖歌の古来の歌唱法に似通っており、ラフマニノフは作曲面でも演奏面でも典礼的性質から逃れられていないのである。
</p>

