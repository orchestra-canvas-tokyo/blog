<p>
  展開部から再現部への移行部には、第1主題を再現するカデンツァが用意されている。本カデンツァは穏やかな「オリジナル」と重厚で長大な「オッシア」の2種類が用意されており、いずれかが演奏される。近年はその壮大な美しさ故か「オッシア」が選ばれる傾向にあるが、「オリジナル」の精緻な旋律も筆舌に尽くし難い美しさを持っている。どちらのカデンツァが演奏されるかで胸が躍る点も本作鑑賞の醍醐味であろう。
</p>

<h4>第2楽章 - Intermezzo</h4>

<p>間奏曲との位置付けに留まらず、即興的・夢想的な様子を感じさせる千変万化の楽章である。</p>

<p>
  3連符が効果的に用いられた幻想的な主要主題を背景に、ピアノが幾度もの転調を行いつつ、ラプソディック、精力的、洒脱なパッセージと目まぐるしく変化するメロディを奏でる。要所では第1楽章第1主題の変奏も垣間見え、華を添える。
</p>

<p>
  コーダに入ると、ピアノが第2楽章との訣別を告げ、和音強奏とグリッサンドの盛り上がりを経て、アタッカで第3楽章に突入する。
</p>

<h4>第3楽章 - Finale</h4>

<p>現代ピアノのあらゆる可能性が詰め込まれた、華やかな大団円へと向かうソナタ形式の楽章である。</p>

<p>
  3連符を効果的に用いて緊張感とダイナミックさを演出する第1主題と、美しく抒情的な第2主題を中心に展開する。それらに、提示部から展開部への経過句で奏でられるフルートとオーボエのアドリブや、展開部での第1楽章第1主題を彷彿とさせる音型が彩りを持たせる。
</p>

<p>
  そしてピアノのジャジーなカデンツァから楽曲が明るく堂々としたコーダに入ると、ラフマニノフ終止と呼ばれる軍隊風の終止で楽曲は締め括られる。ラフマニノフ終止自体は《ピアノ協奏曲第2番》（1901）でも使われた手法であるが、《第2番》では既出のテーマを盛大に奏でるのみであった。一方、本作では楽曲展開の中で発展した音楽を締め括る集大成として、終止を楽曲に不可欠な要素に昇華している。この点において、本作の終止は《第2番》より成熟していると言える。
</p>

<Spacer />

<p>
  ラフマニノフの従来のピアノ協奏曲の課題は、「不完全な形式」や「繰り返しの多用」による直線的な平凡さであった。ラフマニノフはこれらの課題全てに向き合い、本作を端正でロマンティックな音楽作品の極致へと至らしめた。
</p>

<p>これこそ本作が「ラフマニノフの作曲家生涯の真の最重要点」と位置付けられる所以である。</p>

