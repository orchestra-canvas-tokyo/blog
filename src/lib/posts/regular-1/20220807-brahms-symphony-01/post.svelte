<h3>第2楽章 - Andante sostenuto</h3>

<p>
  三部形式の楽章である。第一部で提示される、ヴァイオリンとファゴットによる穏やかで気高い旋律は、オーボエへと受け渡され豊かさを増してゆく。ふたたび現れる弦楽合奏による美しく透明感のある響きが、第二部を導く。木管の細やかな旋律ののちに、全合奏により痛切な句が届けられる。弦楽器を主体とした流麗な経過句に導かれた第三部では、オーボエの旋律に独奏ヴァイオリンと第1ホルンが加わりクライマックスを迎える。美しく儚い透明な空気感のまま、楽曲は消えゆくような終わりを迎える。
</p>

<h3>第3楽章 - Un poco allegretto e grazioso</h3>

<p>
  典雅な間奏曲風の楽章である。軽快なクラリネットにホルンの対旋律が重なり、穏やかな変奏が進行する。中間部は8分の6拍子、依然として軽やかな曲調のなかで、「運命の動機」と「死の動機」（譜例2）の執拗な掛け合いが繰り広げられる。その後、楽曲全体は第4楽章に向けて落ち着きを増してゆき、極めて静かに終わる。
</p>

<Figure src={figure2} caption="譜例2. 「死の動機」" maxHeightPx={65}></Figure>

<p>
  第3楽章の最大の特徴は、従来のスケルツォやメヌエットを採用しなかった点にある。ベートーヴェンの疾走感あふれるスケルツォとの差異は、ブラームスの作品を“内省的”たらしめる理由の一つであろう。
</p>

<h3>第4楽章 - Adagio - Più andante - Allegro non troppo, ma con brio - Più allegro</h3>

<p>
  序奏は二部構成である。第1部では第1楽章を想起させる荘厳で重苦しい旋律が、弦楽器のピツィカート・木管の強奏を経て鎮まる。第2部ではアルペン・ホルンを思わせる朗々としたホルン独奏により一挙に視界が開ける（譜例3）。かねてからブラームスはこの旋律に歌詞をつけており、それは彼が想いを寄せたクララ・シューマンに宛てた手紙に記されたものである。
</p>

<blockquote>
  "Hoch auf’m Berg, tief im Tal, grüß ich dich viel tausendmal!"<br />
  （高い山から、深い谷から、君に何千回も挨拶しよう）
</blockquote>

<p>
  溢れんばかりの想いを秘めつつ、楽曲はトロンボーン・ファゴットの穏やかなコラールを経て序奏の終わりを迎え、「歓喜の歌」を意識した第1主題へと入る。ヴァイオリンにより朗々と奏でられるハ長調の優雅な旋律は、やがて歓喜の嵐へと至る。アルペン風動機の回想により導かれる第2主題ではヴァイオリンの滑らかな旋律が響き、各動機が複雑に絡み合うとホ短調の激しい頂点へと達し、提示部が結ばれる。再現部では、第2主題に続いて第1主題が次々と回想されると、感情は昂ぶり、ピウ・アレグロへと達する。トロンボーンの荘厳なコラールを経て、高らかな歓呼の響きによって楽曲は締め括られる。“暗から明へ”、本作品がベートーヴェンの第10交響曲とも評される所以である。
</p>

