<h3>はじめに</h3>

<p>
  恩師シューマンの勧めにより交響曲第1番の創作に着手したブラームスは、その完成に実に二十年余りを費やした。シューマンは評論『新しい道』（1853）の中でも、ブラームスを讃えつつ、「合唱・オーケストラ曲といった規模の大きな作品においても力を発揮することを期待したい」と記している。他方、決して遅筆ではないブラームスが、心待ちにされた交響曲第1番の完成にこれほどの時間をかけたのは、やはりベートーヴェンの存在を強く意識していたためであろう。「運命の動機」を多分に含む第1楽章、その初稿を書き終えたブラームスはかく言った。
</p>

<blockquote>
  交響曲なんか書くのはごめんだ。あの巨人が、自分の背後にいつも音を立てて行進してやってくる。
</blockquote>

<p>
  難産の末に産み出されたのは、“苦悩から歓喜”へと至る曲想を宿した、ベートーヴェンの第10交響曲とも評される大作である。内省的ながらも力に溢れるブラームスの息吹は、災禍を生きる我々が心に秘めた希望、その表顕たり得るだろう。
</p>

<h3>第1楽章 - Un poco sostenuto – Allegro</h3>

<p>
  バスとティンパニの重々しい響きに支配された序奏では、短調であることが示されたのち、上昇音型と下降音型が半音階で交錯する。荘厳ながらどこか空疎な寂寞感が世界を提示し、オーボエの優しく切ない旋律に導かれ主部へと入る。主部では慌ただしく緊張と弛緩が繰り返されるが、突如、ヴィオラから「運命の動機」が鋭い鼓動のごとく奏でられる（譜例1）。「運命の動機」がヴァイオリンへ移ると楽曲は激しさを増し、闘争の旋律、束の間の康寧を経て、ついには最高点へと達する。全体を通して短調だが、最後はポコ・ソステヌートへと速度を緩めつつ、長調に転じて結ばれる。
</p>

