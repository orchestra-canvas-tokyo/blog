<h3>おわりに</h3>

<p>
  感染症の流行が生（なま）の音楽の価値を問うている。音楽のあり方が変わりゆくことへの危機感は、70年前、円熟期の指揮者フルトヴェングラーによっても表明されていた。彼は二度の世界大戦の先に訪れた物質主義の時代において、人間――純真に献身的に美を追究する心――に宿る芸術の深遠が、理論や技術のみに還元されゆく潮流を、強く忌避したのである。彼の警鐘は、社会批判というよりは、疲れにも近い、ロマン派芸術への愛惜の発露であった。純朴な情熱が表現をより一層自由にする中、高度に秩序整然とした作品を創りつづけたブラームスも例に漏れず、比較芸術論のみでは語り得ない“個”を模索していったのである。
</p>

<p>
  当団の奏者の多くは、感染症でかけがえのない演奏機会を喪った。それでも再びここに集い、各人にとっての「音楽の危機」を乗り越えようとしている。五線譜に紡がれたブラームスの想いが純白な心のCanvasへと映されるとき、生の音楽が鮮やかに立ち現れることを願ってやまない。
</p>

