<h3>交響曲第1番の作曲に至るまで</h3>

<Figure
  src={figureSchonbrunn}
  caption="ハプスブルク王朝の歴代君主が利用したシェーンブルン宮殿 (ウィーン)"
  maxHeightPx={300}
