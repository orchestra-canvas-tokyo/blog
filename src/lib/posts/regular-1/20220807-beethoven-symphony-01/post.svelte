<p>
  本作品は概して、ベートーヴェンのウィーンにおける作曲技法修練の集大成と、独自路線開拓の記念碑的作品と言える。初演は1800年4月、当時29歳のベートーヴェン自身の指揮によって執り行われた。彼は本作品を、ハイドンやモーツァルトの作品、自身の《七重奏曲》などを含むプログラムのトリに配して、満を持す形で世に放ったのである。
