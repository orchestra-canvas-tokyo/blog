<p>
  ドイツのボンで生まれた田舎者の青年が、その後作曲家として大成するウィーンに落ち着いたのは、1792年晩秋のことであった。この時代ハプスブルク家直轄であったウィーンは、諸外国の戦争や革命とは対照的に、王宮の不朽の威容と、音楽を楽しむ貴族たちに特徴付けられた都市であった。当時22歳のベートーヴェンはウィーン到着後、すでに“交響曲の父”として権威ある作曲家であったハイドン、ウィーンにて作曲家として名が広く知られていたヨハン・シェンク、シュテファン大聖堂の指揮者であったアルブレヒツベルガーから、厳格対位法と初めとした作曲技法の基礎を学んだ。また、イタリア歌劇の作曲家であったアントニオ・サリエリより歌曲の作曲を、アイロス・フェルスターより弦楽四重奏の作曲技法を学んでいる。彼はのちに交響曲と弦楽四重奏とを、表現上の手段として異なる役割を持たせていることから、この一連の作曲法習得は、彼に多大な影響を与えたと考えられる。
</p>

