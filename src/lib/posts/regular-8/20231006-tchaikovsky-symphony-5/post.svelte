<h4>第3楽章 Allegro moderato</h4>

<p>
  スケルツォ楽章がおかれるのが主流だが、本作では純然たるワルツが用いられた。ある種バレエ的であり、チャイコフスキーの新たな試みと言える。冒頭、第１ヴァイオリンが第1のワルツとして優雅な旋律を提示する（譜例3）。やや分かりにくいが、この主題も「運命の主題」に関連する下行音階である。第2のワルツはオーボエとファゴットによって奏され、クラリネットへと受け継がれる。第3のワルツはシンコペーションが特徴的なファゴットソロであり、本作の鑑賞における名所の一つである。コーダの後半では再び「運命の主題」が陰鬱に奏でられるが、突如として<MusicalNotation
    content="fortissimo"
  />の和音が現れ、楽章を終わらせる。
</p>

<Figure src={figure3} caption="譜例3" maxHeightPx={90}></Figure>

<h4>第4楽章 Andante maestoso – Allegro vivace</h4>

<p>
  冒頭、「運命の主題」がホ長調で荘厳に奏される(譜例4)。弦楽器の低音が、堂々たる威厳を醸し出し、第1~第3楽章とは異なる決然とした様を唄う。提示部では、再び「運命の主題」に関連する下行音形（第1主題）が現れ、弦楽器が下げ弓による激烈な演奏を行う。曲の勢いは増すばかりで、第2主題（再び「運命の動機」に関連する下行音形を含んでいる）、転調を繰り返す展開部を経て、遂に山場を迎える。コーダに入ると、ホ長調の「運命の主題」がトランペットにより高らかに唄われる。2分の2拍子の急速なプレストを経てから、やや速度を落とし、「運命の主題」を高らかに管楽器群が奏する。最後はホ長調のトニックに力強く終止する。
</p>

