<h4>第2楽章　Kräftig bewegt, doch nicht zu schnell</h4>

<p>
  三部形式の快活なスケルツォである。冒頭の特徴的なリズムは、自身の歌曲《ハンスとグレーテ》に由来しており、素朴な喜びを感じさせる。ホルンの経過句を経てレントラー（南ドイツの民族舞踊）風のトリオに入ると、グリッサンドを織り交ぜた優美でデリケートな旋律が奏でられる。再びホルンの経過句を経ると主部に回帰し、華やかに楽章を終える。
</p>

<h4>第3楽章　Feierlich und gemessen, ohne zu schleppen</h4>

<p>
  第1楽章と同様、４度の下降音型を基調とした冒頭である。独奏コントラバスが奏でる重々しく哀愁を帯びた旋律（譜例3）がカノン風に展開される。この旋律は世界各地で知られるフランス民謡《フレール・ジャック》（日本では《グーチョキパーでなにつくろう》）を短調にしたものであり、グロテスクなだけでなくどこかパロディックな側面も感じさせる。奇怪な進行が続いたのち、突如ヴァイオリンによる牧歌的で安らかな旋律がはじまる。《さすらう若人の歌》第４曲＜彼女の青い目が＞の最後の部分であり、安らかな眠り、すなわち死を暗示している。
</p>

<p>
  ふたたびカノンは再開され、フラストレーションを残したまま、終楽章に切れ目なくアタッカで繋がる。
</p>

<Figure src={figure3} caption="譜例3" maxHeightPx={80}></Figure>

<h4>第4楽章　Stürmisch bewegt</h4>

<p>
  シンバルの強奏で幕開ける第4楽章はソナタ形式に基づいており、地獄を表すヘ短調から、天国を表すニ長調への移動が主軸である。ヘ短調の冒頭はティンパニの乱打と弦楽器の激烈な刻みに支えられ、管楽器が旺然とテーマを奏でる。リストの《ダンテ交響曲》に由来する「十字架の動機（譜例4）」が繰り返し提示され、嵐はなかなか止まない。やがてヴァイオリンによる夢を見ているように優美な第2主題があらわれるが、これは変ニ長調であり、真の天国ではないこと、またすぐに激烈な嵐が訪れることを予感させる。
</p>

<p>
  闘争の果てに一旦ニ長調に達すると、第1楽章の序奏、ついで第2主題が再現され、若干の不安を残しつつも穏やかな旋律が奏でられる。ヴィオラによる「十字架の動機」が地獄の再来を仄めかすが、提示部に比べ音量は小さいままで、むしろ地獄からの脱却を確信させる。そうして今度こそ、確固たる自信を胸に、天国へ、ニ長調へと移行する。第1楽章から多用されてきた4度の下降音形がニ長調のコラールとして提示され、輝かしい勝利宣言によって曲は結ばれる。
</p>

