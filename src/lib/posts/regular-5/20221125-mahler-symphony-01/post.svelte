<h3>おわりに</h3>

<p>
  本作品はその若々しくエネルギッシュな曲想から人気を博しており、演奏機会も非常に多い。しかしマーラー自身は、決して勢いと才能にのみ委ねた新進気鋭の若者ではなく、自分は何者かと問い続けながら生き、人に伝わる楽想とは如何なるものかと問い続けながらペンを握った、“さすらう”若人だったのである。然れば楽譜に書かれた緻密な指示の数々は、奏者を縛るためのものではなく、むしろ楽想とその言語化を相補的に用いることで、時空を越えて形而上を追求するための技芸だったのではなかろうか。
</p>

<p>
  本演奏会をもって、OCTは第5回の節目を迎える。五線譜に紡がれたマーラーの思索が自らの“色”を模索するCanvasと重なるとき、“音の絵”が鮮やかに立ち現れることを願ってやまない。
</p>

