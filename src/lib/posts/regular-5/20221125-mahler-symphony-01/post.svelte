<blockquote>私は三重の意味で故郷がない人間だ。</blockquote>

<p>
  グスタフ・マーラーは1860年、オーストリア帝国のボヘミア地方に生まれた。両親はユダヤ人であり、その出自は彼の人生に大きく影響している。1867年に成立したオーストリア＝ハンガリー二重帝国においてユダヤ人には完全な市民権が認められたが、反ユダヤ主義は未だ根強く、指揮者として活躍した若き日のマーラーも差別に苦しめられていった。36歳でウィーン宮廷歌劇場監督に就任する際、ついにはローマ・カトリックへと改宗することを選んだ。19歳のときに自らを「神秘的なさまよえるユダヤ人」と称したマーラーは、そのアイデンティティを放棄せざるを得なかったのである。
</p>

<p>
  オーストリア人の間ではボヘミア人、ドイツ人の間ではオーストリア人、そして全世界の人々の間ではユダヤ人として、三重の意味で“故郷がない”疎外感を抱えていたマーラー。多様な変遷を遂げた《交響曲第1番》は、自らのアイデンティティを求め“さすらう”マーラーの半生と重なる。
</p>

<h3>《交響曲第1番》に至るまで</h3>

<p>
  ウィーン大学で学んだマーラーは当初、ワーグナーの先を行くような斬新な音楽を目指していた。しかしブラームスらの評価を得られなかったことにより、奏者にも聴衆にもわかりやすい交響曲の作成に着手した。1884年、《交響曲第1番》の前身となる《交響詩》（呼び方は多様である）のスケッチを開始。この間にマーラーは失恋を経験しており、その熱くも報われない想いを現した《さすらう若人の歌》は、《交響詩》において重要なモチーフとして数多く引用されている。
</p>

<p>
  当初《交響詩》は5楽章構成であり、第3楽章までを第1部、第4楽章以降を第2部として、それぞれの楽章に副題がついていた。初稿は初演地の名を取ってブダペスト稿と呼ばれているが、その初演は失敗したと言われている。改訂版であるハンブルク稿およびワイマール稿では、各楽章の標題に加え、楽曲全体に《巨人》という副題が与えられている。これはジャン・パウルの⼩説『巨⼈』から取ったものであるが、そのストーリーと交響曲の中⾝は全く関係がなく、終楽章の華やかな勝利の旋律を“巨人”と安易に結びつけるのは拡大解釈だと言わざるを得ない。
</p>

<p>
  マーラーは本作に不徹底な性格付けがなされることを嫌い、ベルリン稿および最終稿では、各楽章の標題および《巨人》という副題を全て削除した。加えて、楽曲全体の調和を鑑みて《花の章》（ワイマール稿までの第2楽章にあたる）を削除し、現在の形式に至る。
</p>

<h3>楽曲紹介</h3>

<h4>第1楽章　Langsam, Schleppend, wie ein Naturlaut - Im Anfang sehr gemächlich</h4>

<p>
  自由なソナタ形式の楽章である。“Wie ein
  Naturlaut.（⾃然の⾳のように）”と指示されたA音のフラジオレットに始まり、4度の下降音形が展開される。ラッパの遠い音色、カッコウの鳴き声が繰り返される中、ホルンのコラールによって霧が晴れる。チェロによる第1主題（譜例1）は《さすらう若人の歌》第2曲の＜朝の野べを歩けば＞の引用であり、高揚の後に再び静けさを取り戻す。展開部ではやや変則的な進行のもと、第2ヴァイオリンなどにより第4楽章が暗示され（譜例2）、半音進行ののちにクライマックスに達する。ニ長調のファンファーレを経て、全合奏により快活に奏でられる《朝の野べを歩けば》の主題が楽章を締めくくる。
</p>

